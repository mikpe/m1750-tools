/* type.h  --  type shorthands */

#ifndef _TYPE_H
#define _TYPE_H

#ifndef ulong
#define ulong   unsigned long
#endif
#ifndef ushort
#define ushort  unsigned short
#endif
#ifndef uchar
#define uchar   unsigned char
#endif
#ifndef byte
#define byte    unsigned char
#endif
#ifndef bool
#define bool	char
#endif
#ifndef FALSE
#define FALSE	0
#define TRUE	(!FALSE)
#endif

#define elsecase  break;case

#endif

